package hackerrank;
import java.io.IOException;

public class javaLoops1 {
	    public static void main(String[] args) throws IOException {
	        

	        int N = 2;
	        
	        for(int i = 1; i <= 10; i++) {
	        	int resultado = i * N;
	        	System.out.println(N + " x " + i + " = " + resultado);
	        }
	       
	    
	}

}
